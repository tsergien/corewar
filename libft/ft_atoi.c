#include "libft.h"

int			ft_atoi(const char *str)
{
	int			i;
	long long	nb;

	i = 0;
	nb = 0;
	if (!str || !*str)
		return (0);
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	(str[0] == '-' || str[0] == '+') ? i++ : 0;
	while (str[i] > 47 && str[i] < 58)
		nb = nb * 10 + str[i++] - 48;
	(nb < 0) ? nb = 0 : 0;
	((nb > 2147483647 && str[0] != '-') || (nb > 2147483648
	&& str[0] == '-')) ? nb = 0 : 0;
	(str[0] == 45 && nb != 0) ? nb *= -1 : 0;
	return (nb);
}
