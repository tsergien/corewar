#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		n %= 1000000000;
		n *= -1;
	}
	else if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n / 10 == 0)
		ft_putchar(48 + n);
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
