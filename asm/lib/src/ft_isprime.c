#include "libft.h"

int	ft_isprime(size_t nbr)
{
	size_t i;

	if (nbr == 0)
		return (0);
	if (nbr == 1)
		return (0);
	i = 2;
	while (i <= nbr / 2)
	{
		if (!(nbr % i))
			return (0);
		i++;
	}
	return (1);
}
